package Sort.j20220726;

public class For_1 {
	public static void main(String[] args) {
		for (int i = 1; i <= 10; i++) {
			System.out.println(i);
		}
		System.out.println("==============");
		int sum =0;
		for (int i = 1; i <= 10; i++) {
			sum +=i;
		}
		System.out.println(sum);
		System.out.println("==============");
		sum =0;
		for (int i = 2; i <= 11; i++) {
			sum +=i;
		}
		System.out.println(sum);
	}
}
